package org.course;

public class Figura {
    private final double BASE;
    private final double ALTURA;

    public Figura(double base, double altura) {
        this.BASE = base;
        this.ALTURA = altura;
    }

    public double getBASE() {
        return BASE;
    }

    public double getALTURA() {
        return ALTURA;
    }

}
